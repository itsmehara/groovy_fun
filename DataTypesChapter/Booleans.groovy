// Booleans.groovy

// Boolean values
def isTrue = true
def isFalse = false

println "Boolean true: ${isTrue}"
println "Boolean false: ${isFalse}"

// Logical operations
println "Not isTrue: ${!isTrue}"
println "isTrue AND isFalse: ${isTrue && isFalse}"
println "isTrue OR isFalse: ${isTrue || isFalse}"
