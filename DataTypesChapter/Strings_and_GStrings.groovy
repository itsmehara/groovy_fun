// Strings_and_GStrings.groovy

// String
def name = "Alice"
println "String: ${name}"

// GString
def age = 30
def message = "Name: ${name}, Age: ${age}"
println "GString: ${message}"

// Multiline string
def multilineString = """This is a 
multiline string
in Groovy."""
println "Multiline String: ${multilineString}"
